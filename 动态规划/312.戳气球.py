class Solution:
    def maxCoins(self, nums: List[int]) -> int:
        ## è¥é€ ä¸€ä¸ªå¼€åŒºé—´çš„é—®é¢˜
        nums = [1] + nums + [1]
        # è¿˜æ˜¯è¦å®šä¹‰dpæ•°ç»„çš„å«ä¹‰
        # dp[i][j]ä»£è¡¨åœ¨å¼€åŒºé—´(i, j)ä¸‹é¢æŠŠä¸­é—´çš„ğŸˆéƒ½æˆ³çˆ†æ‰€èƒ½è·å¾—çš„æœ€å¤§æ”¶ç›Š
        n = len(nums)
        dp = [[0 for _ in range(n)] for _ in range(n)]
        # æœ€ç»ˆè¦æ±‚çš„å°±æ˜¯dp[0][n-1]
        # ç”±äºè¦æ±‚çš„æœ€ç»ˆç»“æœåœ¨dpè¡¨ä¸­å¤„äºå³ä¸Šè§’
        # æ‰€ä»¥ä¹Ÿèƒ½ç¡®å®šçŠ¶æ€çš„è¿­ä»£æ–¹å‘

        for i in range(n-2, -1, -1):
            for j in range(i+1, n):
                for k in range(i+1, j):
                    dp[i][j] = max(dp[i][j], 
                        dp[i][k] + dp[k][j] + nums[i]*nums[j]*nums[k])

        return dp[0][n-1]
